export const PRESET_MAP: Record<PresetId, PresetMeta> = PRESETS.reduce(
  (acc, p) => {
    acc[p.id] = p;
    return acc;
  },
  {} as Record<PresetId, PresetMeta>
);

export function buildAnimationFromPreset(
  presetId: PresetId,
  layerIds: string[],
  layerDepths: Record<string, number>
): AnimationConfig {
  const preset = PRESET_MAP[presetId];
  const layers: Record<string, LayerAnimationConfig> = {};
  layerIds.forEach((id, i) => {
    const depth = layerDepths[id] ?? 0.5;
    layers[id] = { layerId: id, ...preset.buildLayer(depth, i, layerIds.length) };
  });

  const effects = ALL_EFFECTS.reduce(
    (acc, e) => {
      acc[e] = preset.effects?.[e] ?? false;
      return acc;
    },
    {} as Record<EffectType, boolean>
  );

  return {
    preset: presetId,
    intensity: preset.base.intensity,
    speed: preset.base.speed,
    parallaxEnabled: preset.base.parallaxEnabled,
    liquidEnabled: preset.base.liquidEnabled,
    particlesEnabled: preset.base.particlesEnabled,
    shimmerEnabled: preset.base.shimmerEnabled,
    chromaticAberration: preset.base.chromaticAberration,
    vignette: preset.base.vignette,
    reducedMotion: false,
    renderMode: preset.base.renderMode,
    mouseSmoothing: preset.base.mouseSmoothing,
    perspective: preset.base.perspective ?? 1000,
    rotate3dStrength: preset.base.rotate3dStrength ?? 8,
    scrollParallax: 0.4,
    entranceEnabled: true,
    colorGrade: "none",
    letterbox: false,
    // v3: presets can now enable gateWeave (vintage preset)
    gateWeave: (preset.base as any).gateWeave ?? false,
    dofEnabled: false,
    focusDepth: 0.5,
    aperture: 0.3,
    focusMode: "manual",
    scaleWithDepth: true,
    sceneComposition: "free",
    atmoLightCycle: false,
    atmoFogDrift: false,
    atmoTimelapse: false,
    atmoSeasonal: false,
    depthFogEnabled: false,
    depthFogDensity: 0.4,
    bloomEnabled: false,
    bloomIntensity: 0.5,
    toneMapStrength: 0.6,
    relightingEnabled: false,
    relightingAzimuth: 45,
    relightingElevation: 45,
    relightingIntensity: 0.5,
    relightingColorTemp: 0.3,
    colorScriptEnabled: false,
    colorScriptAct: -1,
    motionBlurEnabled: false,
    motionBlurStrength: 0.5,
    // v3: mathematical motion engine (exact, non-deforming, harmonic)
    useMathEngine: false,
    layers,
    effects,
  };
}
