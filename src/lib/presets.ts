export interface PresetMeta {
  id: PresetId;
  name: string;
  emoji: string;
  tagline: string;
  description: string;
  /** mood-based category for filtering in the UI */
  category: PresetCategory;
  /** builds per-layer config from a base layer (depth 0..1, index) */
  buildLayer: (depth: number, index: number, total: number) => Omit<LayerAnimationConfig, "layerId">;
  base: Pick<
    AnimationConfig,
    | "intensity"
    | "speed"
    | "parallaxEnabled"
    | "liquidEnabled"
    | "particlesEnabled"
    | "shimmerEnabled"
    | "chromaticAberration"
    | "vignette"
    | "renderMode"
    | "mouseSmoothing"
  > & {
    perspective?: number;
    rotate3dStrength?: number;
  };
  effects?: Partial<Record<EffectType, boolean>>;
  /** whether this preset is "advanced" (v2) */
  v2?: boolean;
}


